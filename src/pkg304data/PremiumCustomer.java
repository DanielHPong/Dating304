package pkg304data;

public class PremiumCustomer {
	private int customerId;
	private int infoId;
	
	public PremiumCustomer(int customerId, int infoId) {
		this.customerId = customerId;
		this.infoId = infoId;
	}

	public int getCustomerId() {
		return customerId;
	}
	public int getInfoId() {
		return infoId;
	}
}
